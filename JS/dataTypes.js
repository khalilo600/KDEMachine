// Data Types in JavaScript
// This file demonstrates the various data types available in JavaScript

// 1. Primitive Data Types

// a) Number
let age = 25; // Integer
let price = 19.99; // Floating-point number

// b) String
let name = "John Doe"; // Textual data

// c) Boolean
let isStudent = true; // true or false

// d) Undefined
let address; // Variable declared but not assigned a value

// e) Null
let phoneNumber = null; // Explicitly set to no value

// f) Symbol (ES6)
let uniqueId = Symbol('id'); // Unique and immutable identifier

// g) BigInt (ES2020)
let bigNumber = 9007199254740991n; // For large integers

// 2. Non-Primitive Data Types

// a) Object
let person = {
    firstName: "Jane",
    lastName: "Doe",
    age: 30
};

// b) Array
let fruits = ["Apple", "Banana", "Cherry"];
// c) Function
export function greet() {
    return "Hello, World!";
}
